package pattern_program;

public class Example2 {

	public static void main(String[] args) {
		//* * * * *
		//* * * * *
		//* * * * *
		//* * * * *
		
		//row--->3(outer for loop),column--->5(inner for loop)
		for(int i=1;i<=4;i++)
		{
			for(int j=1;j<=5;j++)
			{
				System.out.print("* ");
			}
			System.out.println();
		}

	}

}

